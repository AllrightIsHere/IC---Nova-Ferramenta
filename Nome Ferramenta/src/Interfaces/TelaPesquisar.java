    public void resetarModel(){
        int i;
        jTextField1.setText("");
        model.removeAllElements();
        for(i = 0; i < telaPrincipal.getID().size(); i++){
            model.addElement(telaPrincipal.getID().get(i).toUpperCase()+" - "+telaPrincipal.getNomes().get(i).toUpperCase());
        }
    }
    
    public boolean listaSelecionada(){
        int i;
        for(i = 0; i < telaPrincipal.getID().size(); i++){
            if(jList1.isSelectedIndex(i))
                return true;
        }
        return false;
    }

