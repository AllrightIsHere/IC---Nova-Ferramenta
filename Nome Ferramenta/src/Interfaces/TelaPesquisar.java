    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        //pesquisa = model;
        model.removeAllElements();
        int i = 0;
        for(String aux : telaPrincipal.getID()){
            if(aux.toUpperCase().startsWith(jTextField1.getText().toUpperCase()) && !model.contains(aux.toUpperCase()+" - "+telaPrincipal.getNomes().get(i))){
                this.model.addElement(aux.toUpperCase()+" - "+telaPrincipal.getNomes().get(i));
            }
            i++;
        }
        i = 0;
        for(String aux : telaPrincipal.getNomes()){
            if(aux.toUpperCase().startsWith(jTextField1.getText().toUpperCase()) && !model.contains(telaPrincipal.getID().get(i)+" - "+aux.toUpperCase())){
                this.model.addElement(telaPrincipal.getID().get(i)+" - "+aux);
            }
            i++;
        }
        jTextField1.setText("");
    }//GEN-LAST:event_searchActionPerformed

    private void detailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailsActionPerformed
        // TODO add your handling code here:
        if(this.listaSelecionada()){
            this.setVisible(false);
            jTextField1.setText("");
            //model = pesquisa;
            if(telaDetalhes == null)
                telaDetalhes = new TelaDetalhes();
            telaDetalhes.setTelaPesquisar(this);
            telaDetalhes.setTelaPrincipal(telaPrincipal);
            telaDetalhes.setTextos(jList1.getSelectedValue());
            telaDetalhes.setVisible(true);
        }
        else{
            JOptionPane.showConfirmDialog(null, "Você não selecionou nenhum item!", "Mensagem de Erro", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_detailsActionPerformed

