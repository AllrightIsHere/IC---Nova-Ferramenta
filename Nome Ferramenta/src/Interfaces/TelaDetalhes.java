    public void setTextos(String detalhe){
        int indice = 0;
        String[] aux = detalhe.split(" - ");
        for(String x : telaPrincipal.getID()){
            if(aux[0].equals(x))
                break;
            indice++;
        }
        textDescription.setText(telaPrincipal.getDescricao().get(indice));
        textName.setText(telaPrincipal.getNomes().get(indice));
        textID.setText(telaPrincipal.getID().get(indice));
        this.indice = indice;
    }

