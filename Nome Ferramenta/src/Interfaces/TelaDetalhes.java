    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showConfirmDialog(null, "Você irá deletar o "+telaPrincipal.getID().get(indice)+" ?", "Mensagem de Alerta", JOptionPane.YES_NO_OPTION);
        if(opcao == JOptionPane.YES_OPTION){
            telaPesquisar.getModel().removeElement(telaPrincipal.getID().get(this.indice)+" - "+telaPrincipal.getNomes().get(this.indice));
            telaPrincipal.removerDado(this.indice);
            telaPesquisar.resetarModel();
            telaPrincipal.setTelaPesquisar(telaPesquisar);
            telaPesquisar.setTelaPrincipal(telaPrincipal);
        }
        this.setVisible(false);
        telaPesquisar.setVisible(true);
    }//GEN-LAST:event_deleteActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        this.setVisible(false);
        if(telaUpdate == null)
            telaUpdate = new TelaUpdate();
        telaUpdate.setTelaPrincipal(telaPrincipal);
        telaUpdate.setTelaPesquisar(telaPesquisar);
        telaUpdate.setIndice(indice);
        telaUpdate.setVisible(true);
    }//GEN-LAST:event_updateActionPerformed

